package Entity;

public class PhieuTra {
    private String maPT;           // Invoice ID (Return Invoice ID)
    private String tenKhachHang;   // Customer Name
    private String sdt;            // Customer Phone Number
    private String tenNhanVien;    // Employee Name
    private String ngayDoi;        // Date of the Return
    private String lyDo;           // Reason for the Return
    private float tongTien;        // Total Amount

    // Getters and Setters
    public String getMaPT() {
        return maPT;
    }

    public void setMaPT(String maPT) {
        this.maPT = maPT;
    }

    public String getTenKhachHang() {
        return tenKhachHang;
    }

    public void setTenKhachHang(String tenKhachHang) {
        this.tenKhachHang = tenKhachHang;
    }

    public String getSdt() {
        return sdt;
    }

    public void setSdt(String sdt) {
        this.sdt = sdt;
    }

    public String getTenNhanVien() {
        return tenNhanVien;
    }

    public void setTenNhanVien(String tenNhanVien) {
        this.tenNhanVien = tenNhanVien;
    }

    public String getNgayDoi() {
        return ngayDoi;
    }

    public void setNgayDoi(String ngayDoi) {
        this.ngayDoi = ngayDoi;
    }

    public String getLyDo() {
        return lyDo;
    }

    public void setLyDo(String lyDo) {
        this.lyDo = lyDo;
    }

    public float getTongTien() {
        return tongTien;
    }

    public void setTongTien(float tongTien) {
        this.tongTien = tongTien;
    }
}

