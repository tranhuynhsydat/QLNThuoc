package Entity;

import java.util.Objects;

/**
 *
 * @author roxan
 */
class VaiTro {
    private String id;
    private String ten;
	public VaiTro(String id, String ten) {
		super();
		this.id = id;
		this.ten = ten;
	}
	public VaiTro() {
		super();
	}
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getTen() {
		return ten;
	}
	public void setTen(String ten) {
		this.ten = ten;
	}
	@Override
	public int hashCode() {
		return Objects.hash(id);
	}
	@Override
	public boolean equals(Object obj) {
		if (this == obj)
			return true;
		if (obj == null)
			return false;
		if (getClass() != obj.getClass())
			return false;
		VaiTro other = (VaiTro) obj;
		return Objects.equals(id, other.id);
	}
    
}
